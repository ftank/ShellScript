#!/bin/bash

echo '############################################################'
echo '################### Starting daily checks ##################' 
echo '############################################################'

echo ' How many Racks are you going to check?'
read n
	for ((i=1 ; i<=$n ; i++))
		do
			echo 'Specify the Rack name:'  
			read rack
			echo 'STBs:'  
			read stb
			echo '############################################################' 
			echo '##########################' $rack '###########################' 
			echo '############################################################' 
			ssh -q user@$rack "~/introproSRC/new_check_stb.py $stb" 
			echo
		done
echo '############################################################'
echo '################### Daily checks completed ##################'
echo '############################################################'

exit 0
